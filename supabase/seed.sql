-- randomly assign picks for each user
DO $$
DECLARE u RECORD;
BEGIN FOR u IN
SELECT id
FROM auth.users LOOP WITH favorite AS (
    SELECT bo.id,
      g.id AS game_id
    FROM bet_options bo
      JOIN games g ON bo.game_id = g.id
    WHERE g.week = 1
      AND bo.type = 'spread'
      AND bo.line < 0
    ORDER BY RANDOM()
    LIMIT 1
  ), underdog AS (
    SELECT bo.id
    FROM bet_options bo
      JOIN games g ON bo.game_id = g.id
    WHERE g.week = 1
      AND bo.type = 'spread'
      AND bo.line > 0
      AND g.id NOT IN (
        SELECT game_id
        FROM favorite
      )
    ORDER BY RANDOM()
    LIMIT 1
  ), total AS (
    SELECT bo.id
    FROM bet_options bo
      JOIN games g ON bo.game_id = g.id
    WHERE g.week = 1
      AND bo.type = 'total'
    ORDER BY RANDOM()
    LIMIT 1
  )
INSERT INTO picks (user_id, bet_option_id)
SELECT u.id,
  id
FROM favorite
UNION ALL
SELECT u.id,
  id
FROM underdog
UNION ALL
SELECT u.id,
  id
FROM total;
END LOOP;
END $$;